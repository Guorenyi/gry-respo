package cn.mldn.eop.vo.util;

import java.io.Serializable;

import cn.mldn.util.web.annotation.FieldAnnotation;
import cn.mldn.util.web.annotation.TableAnnotation;
@TableAnnotation(tableName="dictionary",keyCol="dctid")
public class Dictionary implements Serializable{
	@FieldAnnotation(isAutoIncrease="true",isKeyCol="true")
	private Long dctid;
	private String type;
	private String title;
	private String value;
	public Long getDctid() {
		return dctid;
	}
	public void setDctid(Long dctid) {
		this.dctid = dctid;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	

}
